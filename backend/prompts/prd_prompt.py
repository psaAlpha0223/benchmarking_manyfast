TEMPLATE = """당신은 IT 서비스 기획 전문가입니다.
아래 프로젝트 설명, 주요 기능, 질문 응답 내용을 바탕으로 PRD를 마크다운 형식으로 작성하세요.

[작성 원칙]
- 추측이 필요한 부분은 [가정: ...]으로 명시하세요.
- 비개발자도 이해할 수 있도록 쉬운 언어로 작성하되, 기술 스택 항목은 아래 고정값을 그대로 사용하세요.
- 한국어로 작성하세요.

[고정 기술 스택 — 반드시 아래 값을 그대로 사용할 것]
- 프론트엔드: Next.js
- 백엔드: FastAPI
- AI: Anthropic Claude API (claude-sonnet-4-6)
- 스타일링: Tailwind CSS

반드시 아래 8개 항목을 모두 포함하세요:

---

## 1. 프로젝트 개요
### 1.1 목적
(서비스 한 줄 정의 / 제품 목표 / 배경 / 문제 및 해결 방안)

### 1.2 기술 스택
| 영역 | 기술 |
(위 고정 기술 스택을 테이블로 작성)

---

## 2. 기능 요구사항
(주요 기능을 중심으로 세부 기능 목록 작성. 필수 / 중요 / 선택 우선순위 포함)
- 필수: 없으면 서비스가 동작하지 않는 핵심 기능
- 중요: 있으면 좋지만 없어도 출시 가능한 기능
- 선택: 여유가 있을 때 추가할 기능

---

## 3. UX/UI 요구사항
### 3.1 레이아웃 구조
(주요 화면 구성을 트리 또는 텍스트로 표현)

### 3.2 인터랙션 원칙
(사용자 흐름 상 주요 인터랙션 명시)

### 3.3 디자인 원칙
(톤앤매너, 컴포넌트 스타일 방향)

---

## 4. 폴더 구조
(frontend / backend 디렉토리 트리. 고정 기술 스택 기준으로 작성)

---

## 5. API 설계
### 5.1 엔드포인트 목록
| Method | Path | 설명 |
(테이블 형식)

### 5.2 주요 엔드포인트 상세
(요청/응답 형식을 JSON 예시로 작성)

---

## 6. Claude API 프롬프트 명세
(기능별 시스템 프롬프트 및 유저 프롬프트 작성. 입력 변수는 {{변수명}} 형식으로 표기)

---

## 7. 환경변수 목록
| 변수명 | 예시 값 | 설명 |
(테이블 형식. ANTHROPIC_API_KEY 반드시 포함)

---

## 8. 실행 방법
(Backend 실행 / Frontend 실행 순서를 bash 명령어 포함하여 단계별로 작성)

---
[주요 기능]
{features}

[질문 응답 내용]
{interview_answers}

[프로젝트 설명]
{user_input}"""


def build_prompt(features: list[str], user_input: str, interview_answers: str) -> str:
    return TEMPLATE.format(
        features=", ".join(features),
        interview_answers=interview_answers or "(응답 없음)",
        user_input=user_input or "(자유 텍스트 없음, 첨부 파일 참고)",
    )
